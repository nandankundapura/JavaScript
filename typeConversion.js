let val;

//Number to a String
val=5;
console.log(val);
console.log(typeof val);
console.log(val.length);

//to string
val='5';
console.log(val);
console.log(typeof val);
console.log(val.length);

//string to number
val=5;
console.log(val);
console.log(typeof val);
console.log(val.toFixed(2));


//boolean value  true
val=Number(true);
console.log(val);
console.log(typeof val);
console.log(val.toFixed(2));

//boolean value  false
val=Number(false);
console.log(val);
console.log(typeof val);
console.log(val.toFixed(2));

// to null
val=Number(null);
console.log(val);
console.log(typeof val);
console.log(val.toFixed(2));

// to null
val=parseInt("5");
console.log(val);
console.log(typeof val);
console.log(val.toFixed(2));